DROP TABLE IF EXISTS BOOK;

CREATE TABLE IF NOT EXISTS BOOK(
  ID SERIAL PRIMARY KEY,
  TITLE VARCHAR(64) NOT NULL,
  AUTHOR VARCHAR(64) NOT NULL,
  ISBN ISBN,
  RATING INT CHECK(RATING>=0 AND RATING<=10),
  RELEASE_DATE DATE,
  PUBLISHER VARCHAR(64)
);

